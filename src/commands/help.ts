// /help — что умеет бот и список команд.

import type { TelegramMessage } from "../types";
import { sendMessage } from "../lib/telegram";

const HELP =
	"ℹ️ <b>Книжный клуб — помощь</b>\n\n" +
	"Я помогаю повторять материал книг клуба карточками по алгоритму SM-2: " +
	"каждый день в 10:00 МСК присылаю карточки по всем книгам — по одной, как диалог. " +
	"Жми «Показать ответ» и оценивай, насколько легко вспомнил, — я подберу интервал.\n\n" +
	"<b>Карточки</b>\n" +
	"/today — начать повторение прямо сейчас\n" +
	"/status — статистика по всем книгам\n" +
	"/settings — сколько карточек в день\n" +
	"/stop — отписаться от рассылки\n\n" +
	"<b>Доклады</b>\n" +
	"/speaker — выступить с докладом (выбор темы из плана)\n" +
	"/cancel — прервать заявку на доклад\n\n" +
	"<b>Встречи</b>\n" +
	"Запись на встречи и план — в приложении клуба (кнопки «Пойду» и «Стать спикером» ведут сюда).\n\n" +
	"/help — показать это сообщение";

export async function handleHelp(env: Env, message: TelegramMessage): Promise<void> {
	await sendMessage(env.BOT_TOKEN, message.chat.id, HELP);
}
